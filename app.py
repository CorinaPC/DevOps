import math

def somar(num_1,num_2):
    return num_1+num_2

def subtrair(num1,num2):
    return num1-num2

def multiplicacao(num_1,num_2):
    return num_1*num_2

def divisao(num_1,num_2):
    return num_1/num_2

def raiz_quadrada(num_1):
    return math.sqrt(num_1)

def potencia(num_1,num_2):
    return math.pow(num_1,num_2)

def seno(num_1):
    return math.sin(num_1)

def tangente(num_1):
    return math.tan(num_1)

def cosseno(num_1):
    return math.cos(num_1)